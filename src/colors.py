Y = '\033[93m'   # yellow
C = '\033[96m'   # cyan
W = '\033[97m'   # white
B = '\033[1m'    # bold
D = '\033[90m'   # dim/gray
M = '\033[95m'   # magenta
BL = '\033[5m'   # blink
X = '\033[0m'    # reset
